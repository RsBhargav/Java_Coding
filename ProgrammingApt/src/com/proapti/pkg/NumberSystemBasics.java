package com.proapti.pkg;

public class NumberSystemBasics {

	public static void main(String[] args) {
	// the output here is diplayed equivalent to decimal value the S.O.P statement is doing conversion here... 
		
		int binary = 0B101;   
		System.out.println("Binary = "+binary);
		
		int octal = 013;
		System.out.println("Octal = "+octal);
		
		int hexa = 0XF;
		System.out.println("HexaDecimal = "+hexa);
		
		//by default what we write are decimal values only...

	}

}
